<h1 class="text-4xl font-extrabold tracking-tight scroll-m-20 lg:text-5xl">Serwis Interhome</h1>
<p class="leading-7">
	Serwis INTERHOME świadczy kompleksowe usługi w zakresie napraw samochodów wszystkich typów i
	marek, zarówno osobowych, dostawczych jak i ciężarowych. Naszą dewizą jest fachowość i rzetelność
	w podejściu do klienta. Wykwalifikowany zespół pracowników i odpowiednio wyposażony warsztat
	stanowią podstawę usług świadczonych na najwyższym poziomie. Nasza wieloletnia praktyka związana z
	naprawami pojazdów czyni nas fachowcami w branży motoryzacyjnej.
</p>

<p class="leading-7">
	Wszystkie naprawy które realizujemy w naszym serwisie przebiegają zgodnie z zaleceniami
	producentów pojazdów. Naprawy samochodów zawsze wykonywane są z wykorzystaniem najlepszej jakości
	części zamiennych i najnowocześniejszej technologii.
</p>

<p class="leading-7">
	W trosce o czas naszych klientów, proponujemy możliwie krótkie terminy realizacji usług. Dbamy o
	to aby naprawy były wykonywane na czas. Nasz serwis specjalizuje się w naprawach układów
	doładowania powietrza w samochodach wyposażonych w turbosprężarkę, zarówno w przypadku silników
	diesla jak i benzynowych. Posiadamy bogate zaplecze naprawcze i diagnostyczne w postaci prężnie
	działającego działu regeneracji turbosprężarek i zaawansowanego sprzętu pomiarowego
	umożliwiającego sprawne przeprowadzenie naprawy.
</p>

<p class="leading-7">
	Serwis INTERHOME jako jedyny w Ciechanowie oferuje regenerację turbosprężarek na miejscu przy
	zachowaniu wysokich norm jakościowych oferowanej usługi. Wieloletnie doświadczenie w tym sektorze
	wyróżnia Nasz serwis na tle konkurencji.
</p>

<p class="leading-7">
	Twój samochód uległ awarii? Poszukujesz dobrego mechanika w Ciechanowie, który zajmie się Twoim
	samochodem? INTERHOME to profesjonalny serwis, który zadba o Twój pojazd. Sprawny samochód to nie
	tylko gwarancja komfortu i przyjemnej jazdy, ale także bezpieczeństwa Twojego i Twoich
	najbliższych.
</p>

<p class="leading-7">
	Kompleksowa obsługa różnorodnych aut w naszym serwisie obejmuje szeroką ofertę mechaniki
	pojazdowej i diagnostyki. Dodatkowo w naszym serwisie sprawdzisz samochód przed zakupem.
</p>

